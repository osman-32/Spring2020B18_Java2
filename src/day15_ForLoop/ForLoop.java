package day15_ForLoop;

public class ForLoop {
    public static void main(String[] args) {

        for (int i=0; i<100; i+=2){
            System.out.println("Hello World!");
        }

        for (int i=0; i<11; i++){
            System.out.println(i);
        }

        for (int i=1; i<11; i++){
            System.out.println(i+":"+i*i);
        }
    }
}
